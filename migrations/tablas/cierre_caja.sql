CREATE TABLE cierre_caja(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   fecha timestamp without time zone,
   billete_100 double precision,
   billete_50 double precision,
   billete_20 double precision,
   billete_10 double precision,
   billete_5 double precision,
   billete_2 double precision,
   billete_1 double precision,
   moneda_100 double precision, 
   moneda_50 double precision,
   moneda_25 double precision,
   moneda_10 double precision,
   moneda_5 double precision,
   moneda_1 double precision,
   total_billetes double precision,
   total_monedas double precision,
   total_caja double precision,
   estado varchar,
   sesion_id bigint,
   empresa_id bigint,
   fecha_creacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP, 
   fecha_actualizacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP,
   PRIMARY KEY (id)
);