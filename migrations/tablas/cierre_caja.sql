CREATE TABLE cierre_caja(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   fecha timestamp without time zone,
   billete100 double precision,
   billete50 double precision,
   billete20 double precision,
   billete10 double precision,
   billete5 double precision,
   billete2 double precision,
   billete1 double precision,
   moneda100 double precision, 
   moneda50 double precision,
   moneda25 double precision,
   moneda10 double precision,
   moneda5 double precision,
   moneda1 double precision,
   total_billetes double precision,
   total_monedas double precision,
   total_caja double precision,
   estado varchar,
   fecha_creacion	timestamp without time zone NOT NULL, 
   fecha_actualizacion	timestamp without time zone NOT NULL,
   sesion_id bigint,
   empresa_id bigint,
   PRIMARY KEY (id)
);