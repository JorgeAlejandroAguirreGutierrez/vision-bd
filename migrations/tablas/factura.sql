CREATE TABLE factura(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   establecimiento varchar,
   punto_venta varchar,
   secuencial varchar,
   numero_comprobante varchar,
   codigo_numerico varchar,
   fecha timestamp without time zone,
   fecha_autorizacion timestamp without time zone,
   clave_acceso varchar,
   estado varchar,
   proceso_sri varchar,
   valor_descuento_total double precision,
   porcentaje_descuento_total double precision,
   valor_porcentaje_descuento_total double precision,
   valor_descuento_subtotal double precision,
   porcentaje_descuento_subtotal double precision,
   valor_porcentaje_descuento_subtotal double precision,
   subtotal double precision,
   descuento double precision,
   subtotal_gravado double precision,
   subtotal_no_gravado double precision,
   importe_iva double precision,
   total double precision,
   comentario varchar,
   cliente_id bigint,
   sesion_id bigint,
   tipo_comprobante_id bigint,
   empresa_id bigint,
   total_recaudacion double precision,
   por_pagar double precision,
   efectivo double precision,
   cambio double precision,
   total_cheques double precision,
   total_depositos double precision,
   total_transferencias double precision,
   total_tarjetas_debitos double precision,
   total_tarjetas_creditos double precision,
   total_credito double precision,
   credito_id bigint,
   fecha_creacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP, 
   fecha_actualizacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP,
   PRIMARY KEY (id)
);