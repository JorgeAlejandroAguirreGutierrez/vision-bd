CREATE TABLE cliente(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   identificacion	varchar,
   razon_social 	varchar,
   obligado_contabilidad	varchar,
   especial	varchar,
   estado	varchar,
   direccion	varchar,
   etiqueta	varchar,
   referencia	varchar,
   latitudgeo	varchar,
   longitudgeo	varchar,
   monto_financiamiento	double precision,
   fecha_creacion	timestamp without time zone NOT NULL, 
   fecha_actualizacion	timestamp without time zone NOT NULL,
   tipo_identificacion_id	bigint,
   tipo_contribuyente_id	bigint,
   grupo_cliente_id	bigint,
   forma_pago	bigint,
   plazo_credito_id	bigint,
   ubicacion_id bigint,
   genero_id	bigint,
   estado_civil_id	bigint,
   calificacion_cliente_id	bigint,
   origen_ingreso_id	bigint,
   segmento_id	bigint,
   empresa_id	bigint,
   PRIMARY KEY (id)
);