CREATE TABLE nota_credito_compra_linea(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   cantidad_compra bigint,
   costo_unitario_compra double precision,
   cantidad bigint,
   costo_unitario double precision,
   subtotal_linea double precision,
   importe_iva_linea double precision,
   total_linea double precision,
   fecha_creacion	timestamp without time zone NOT NULL, 
   fecha_actualizacion	timestamp without time zone NOT NULL,
   impuesto_id bigint,
   producto_id bigint,
   bodega_id bigint,
   nota_credito_compra_id bigint,
   PRIMARY KEY (id)
);