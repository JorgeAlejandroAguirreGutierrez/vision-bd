CREATE TABLE guia_remision(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   establecimiento varchar,
   punto_venta varchar,
   secuencial varchar,
   numero_comprobante varchar,
   codigo_numerico varchar,
   clave_acceso varchar,
   fecha timestamp without time zone,
   fecha_autorizacion timestamp without time zone,
   fecha_inicio_transporte timestamp without time zone,
   fecha_fin_transporte timestamp without time zone,
   motivo_traslado varchar,
   ruta varchar,
   identificacion_destinatario varchar,
   razon_social_destinatario varchar,
   direccion_destinatario varchar,
   telefono_destinatario varchar,
   celular_destinatario varchar,
   correo_destinatario varchar,
   opcion_guia varchar,
   estado varchar,
   proceso_sri varchar,
   tipo_comprobante_id bigint,
   sesion_id bigint,
   transportista_id bigint,
   vehiculo_id bigint,
   factura_id bigint,
   empresa_id bigint,
   fecha_creacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP, 
   fecha_actualizacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP,
   PRIMARY KEY (id)
);