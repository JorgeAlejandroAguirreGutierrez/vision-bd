CREATE TABLE factura_compra_linea(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   posicion bigint,
   cantidad bigint,
   costo_unitario double precision,
   costo_distribuido double precision,
   costo_promedio double precision,
   valor_descuento_linea double precision,
   porcentaje_descuento_linea double precision,
   valor_porcentaje_descuento_linea double precision,
   valor_descuento_total_linea double precision,
   valor_porcentaje_descuento_total_linea double precision,
   subtotal_linea_sin_descuento double precision,
   subtotal_linea double precision,
   importe_iva_linea double precision,
   total_linea double precision,
   impuesto_id bigint,
   producto_id bigint,
   bodega_id bigint,
   factura_compra_id bigint,
   fecha_creacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP, 
   fecha_actualizacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP,
   PRIMARY KEY (id)
);