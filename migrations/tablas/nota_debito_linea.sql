CREATE TABLE nota_debito_linea(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   posicion bigint,
   entregado varchar,
   consginacion varchar,
   cantidad bigint,
   precio_unitario double precision,
   valor_descuento_linea double precision,
   porcentaje_descuento_linea double precision,
   valor_porcentaje_descuento_linea double precision,
   subtotal_linea varchar,
   importe_iva_linea double precision,
   total_linea double precision,
   fecha_creacion	timestamp without time zone NOT NULL, 
   fecha_actualizacion	timestamp without time zone NOT NULL,
   impuesto_id bigint,
   precio_id bigint,
   producto_id bigint,
   nota_debito_id bigint,
   PRIMARY KEY (id)
);