CREATE TABLE paquete(
   id       bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   codigo 	varchar,
   nombre 	varchar,
   minimo 	bigint,
   maximo 	bigint,
   valor_total    double precision,
   valor_anual    double precision,
   valor_minimo   double precision,
   valor_maximo   double precision,
   valor_puesta_inicial double precision,
   comision      double precision,
   cantidad_usuario_recaudacion bigint,
   cantidad_usuario_gerente bigint,
   cantidad_usuario_administrador bigint,
   descripcion_promocion   varchar,
   valor_promocion   double precision,
   tipo   varchar,
   estado	varchar,
   fecha_creacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP, 
   fecha_actualizacion	timestamp without time zone NOT NULL default CURRENT_TIMESTAMP,
   PRIMARY KEY (id)
);